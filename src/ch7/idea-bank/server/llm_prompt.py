"""LLMへのプロンプトを定義するモジュール"""
# システムプロンプトの宣言 --- (*1)
LLM_SYSTEM_PROMPT = """\
あなたはアイデア整理アシスタントです。
入力テキストを短く要約し、タイトルとタグを抽出してください。
また、要件に従って、SCAMPERの観点から発展案を考えてください。
そして、JSON形式で結果を返してください。
"""
# アイデア分析のためのユーザープロンプトの宣言 --- (*2)
IDEA_ANALYSIS_USER_PROMPT = """\
次の文章を解析してください。

[要件]
- title: 30文字以内
- summary: 120文字以内
- tags: 3〜5個
- ideas: 発展できるアイデアをSCAMPERを活用して5個考えてください。

[出力形式]
JSONのみを返してください。
{
  "title": "...",
  "summary": "...",
  "tags": ["...", "..."],
  "ideas": ["...", "...", "...", "...", "..."]
}
"""
# ユーザープロンプトを組み立てる関数 --- (*3)
def build_analysis_prompt(text: str) -> str:
    """アイデア分析のためのユーザープロンプトを組み立てる"""
    return f"""{IDEA_ANALYSIS_USER_PROMPT}

[入力テキスト]
{text}
"""
